# 变量指向存储在内存中的值的一个指针。
# 这就意味着在创建变量时，如果初始化的值，在内存中不存在，会在内存中开辟一个空间。
# 这就是为什么Python的变量可以指向任何类型的数据的原因
a = 's'
b = 's'
print(id(a))
print(id(b))

# 变量的命名规则（不要使用关键字）
# 以字母开始的字符串（一般只包含字母数字，下划线）
# 变量的作用是用来存储某一个值
a1 = 1
print(a1)
